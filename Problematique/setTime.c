void TIME_Change(unsigned int *seconde,unsigned int *debounce)
{
    if((BTN_GetValue(1) == 1) && (debounce[2] == 0))
    {
        *seconde += 3600;
        debounce[2] = 1;
    }
    if((BTN_GetValue(2)== 1) && (debounce[1] == 0))
    {
        *seconde += 60;
        debounce[1] = 1;
    }
    if((BTN_GetValue(3)== 1) && (debounce[0] == 0))
    {
        *seconde += 1;
        debounce[0] = 1;
    }
}

